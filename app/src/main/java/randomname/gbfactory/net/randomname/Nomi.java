package randomname.gbfactory.net.randomname;

public class Nomi {

    private int mImage;
    private String mName;

    public Nomi(int mImage, String mName) {
        this.mImage = mImage;
        this.mName = mName;
    }

    public int getmImage() {
        return mImage;
    }

    public String getmName() {
        return mName;
    }

}
